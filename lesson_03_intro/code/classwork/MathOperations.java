package classwork;

public class MathOperations
{

    public static void main(String[] args)
    {
    int x = 38;
    int y = 63;
    int res = x + y;
    int res2 = y - x;

    System.out.println(x + " плюс " + y + " = " + res);
    System.out.println(y + " минус " + x + " = " + res2);

    res = y / x;
    res2 = y % x;

    System.out.println(y + " делить на цело " + x + " = " + res);
    System.out.println(y + " остаток от деления на " + x + " = " + res2);

    }



}




